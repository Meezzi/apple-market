class Product {
  final String imageFile;
  final String title;
  final String description;
  final String seller;
  final int price;
  final String address;
  final int likes;
  final int chats;

  Product({
    required this.imageFile,
    required this.title,
    required this.description,
    required this.seller,
    required this.price,
    required this.address,
    required this.likes,
    required this.chats,
  });
}
